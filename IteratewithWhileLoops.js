var myArray = [];
var i = 0;
while (i < 5) {
    myArray.push(i);
    i++;
}

console.log(myArray);

// Iterate with For Loops

var ourArray = [];
for (var i = 0; i < 5; i++){
    ourArray.push(i);  
}

var theArray = [];

for (var i = 1; i < 6; i++) {
    theArray.push(i);
}

console.log(theArray);